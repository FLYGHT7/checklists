def translate(text, language):
    """
    Traduce un texto al idioma especificado
    
    Args:
        text (str): Texto a traducir
        language (str): Código de idioma ('es' o 'en')
        
    Returns:
        str: Texto traducido o el texto original si no hay traducción
    """
    if language == 'en':
        return text
    
    if language == 'es':
        return TRANSLATIONS_ES.get(text, text)
    
    return text